function theta=NeuralNetwork_Train(trainingDataset)
    tic
    %theta=zeros(1,6);
    x1=trainingDataset(:,1);
    x2=trainingDataset(:,2);
    x3=trainingDataset(:,3);
    x4=trainingDataset(:,4);
    x5=trainingDataset(:,5);
    y=trainingDataset(:,6);
    m=size(trainingDataset,1);   %number of rows
    iterations=10000;
    alpha=0.60;
    %theta=zeros(42,1);
    theta=randi([-10,10],[42,1]);
    theta=double(theta);
    for i=1:m
        for j=1:iterations
            %Forward Propagation Start%
            net_a=theta(1)*x1(i)+theta(2)*x2(i)+theta(3)*x3(i)+theta(4)*x4(i)+theta(5)*x5(i);
            net_b=theta(6)*x1(i)+theta(7)*x2(i)+theta(8)*x3(i)+theta(9)*x4(i)+theta(10)*x5(i);
            net_c=theta(11)*x1(i)+theta(12)*x2(i)+theta(13)*x3(i)+theta(14)*x4(i)+theta(15)*x5(i);
            net_d=theta(16)*x1(i)+theta(17)*x2(i)+theta(18)*x3(i)+theta(19)*x4(i)+theta(20)*x5(i);
            net_e=theta(21)*x1(i)+theta(22)*x2(i)+theta(23)*x3(i)+theta(24)*x4(i)+theta(25)*x5(i);
            net_f=theta(26)*x1(i)+theta(27)*x2(i)+theta(28)*x3(i)+theta(29)*x4(i)+theta(30)*x5(i);
            net_g=theta(31)*x1(i)+theta(32)*x2(i)+theta(33)*x3(i)+theta(34)*x4(i)+theta(35)*x5(i);
            out_a=sigmoid(net_a);
            out_b=sigmoid(net_b);
            out_c=sigmoid(net_c);
            out_d=sigmoid(net_d);
            out_e=sigmoid(net_e);
            out_f=sigmoid(net_f);
            out_g=sigmoid(net_g);
            net_o=theta(36)*out_a+theta(37)*out_b+theta(38)*out_c+theta(39)*out_d+theta(40)*out_e+theta(41)*out_f+theta(42)*out_g;
            out_o=sigmoid(net_o);
            %Forward Propagation End%
            %Backward Propagation Start%
            temp_36=out_a*out_o*(1-out_o)*(out_o-y(i));
            temp_37=out_b*out_o*(1-out_o)*(out_o-y(i));
            temp_38=out_c*out_o*(1-out_o)*(out_o-y(i));
            temp_39=out_d*out_o*(1-out_o)*(out_o-y(i));
            temp_40=out_e*out_o*(1-out_o)*(out_o-y(i));
            temp_41=out_f*out_o*(1-out_o)*(out_o-y(i));
            temp_42=out_g*out_o*(1-out_o)*(out_o-y(i));
            temp_1=x1(i)*out_a*(1-out_a)*(out_o-y(i))*out_o*(1-out_o)*theta(36);
            temp_2=x2(i)*out_a*(1-out_a)*(out_o-y(i))*out_o*(1-out_o)*theta(36);
            temp_3=x3(i)*out_a*(1-out_a)*(out_o-y(i))*out_o*(1-out_o)*theta(36);
            temp_4=x4(i)*out_a*(1-out_a)*(out_o-y(i))*out_o*(1-out_o)*theta(36);
            temp_5=x5(i)*out_a*(1-out_a)*(out_o-y(i))*out_o*(1-out_o)*theta(36);
            temp_6=x1(i)*out_b*(1-out_b)*(out_o-y(i))*out_o*(1-out_o)*theta(37);
            temp_7=x2(i)*out_b*(1-out_b)*(out_o-y(i))*out_o*(1-out_o)*theta(37);
            temp_8=x3(i)*out_b*(1-out_b)*(out_o-y(i))*out_o*(1-out_o)*theta(37);
            temp_9=x4(i)*out_b*(1-out_b)*(out_o-y(i))*out_o*(1-out_o)*theta(37);
            temp_10=x5(i)*out_b*(1-out_b)*(out_o-y(i))*out_o*(1-out_o)*theta(37);
            temp_11=x1(i)*out_c*(1-out_c)*(out_o-y(i))*out_o*(1-out_o)*theta(38);
            temp_12=x2(i)*out_c*(1-out_c)*(out_o-y(i))*out_o*(1-out_o)*theta(38);
            temp_13=x3(i)*out_c*(1-out_c)*(out_o-y(i))*out_o*(1-out_o)*theta(38);
            temp_14=x4(i)*out_c*(1-out_c)*(out_o-y(i))*out_o*(1-out_o)*theta(38);
            temp_15=x5(i)*out_c*(1-out_c)*(out_o-y(i))*out_o*(1-out_o)*theta(38);
            temp_16=x1(i)*out_d*(1-out_d)*(out_o-y(i))*out_o*(1-out_o)*theta(39);
            temp_17=x2(i)*out_d*(1-out_d)*(out_o-y(i))*out_o*(1-out_o)*theta(39);
            temp_18=x3(i)*out_d*(1-out_d)*(out_o-y(i))*out_o*(1-out_o)*theta(39);
            temp_19=x4(i)*out_d*(1-out_d)*(out_o-y(i))*out_o*(1-out_o)*theta(39);
            temp_20=x5(i)*out_d*(1-out_d)*(out_o-y(i))*out_o*(1-out_o)*theta(39);
            temp_21=x1(i)*out_e*(1-out_e)*(out_o-y(i))*out_o*(1-out_o)*theta(40);
            temp_22=x2(i)*out_e*(1-out_e)*(out_o-y(i))*out_o*(1-out_o)*theta(40);
            temp_23=x3(i)*out_e*(1-out_e)*(out_o-y(i))*out_o*(1-out_o)*theta(40);
            temp_24=x4(i)*out_e*(1-out_e)*(out_o-y(i))*out_o*(1-out_o)*theta(40);
            temp_25=x5(i)*out_e*(1-out_e)*(out_o-y(i))*out_o*(1-out_o)*theta(40);
            temp_26=x1(i)*out_f*(1-out_f)*(out_o-y(i))*out_o*(1-out_o)*theta(41);
            temp_27=x2(i)*out_f*(1-out_f)*(out_o-y(i))*out_o*(1-out_o)*theta(41);
            temp_28=x3(i)*out_f*(1-out_f)*(out_o-y(i))*out_o*(1-out_o)*theta(41);
            temp_29=x4(i)*out_f*(1-out_f)*(out_o-y(i))*out_o*(1-out_o)*theta(41);
            temp_30=x5(i)*out_f*(1-out_f)*(out_o-y(i))*out_o*(1-out_o)*theta(41);
            temp_31=x1(i)*out_g*(1-out_g)*(out_o-y(i))*out_o*(1-out_o)*theta(42);
            temp_32=x2(i)*out_g*(1-out_g)*(out_o-y(i))*out_o*(1-out_o)*theta(42);
            temp_33=x3(i)*out_g*(1-out_g)*(out_o-y(i))*out_o*(1-out_o)*theta(42);
            temp_34=x4(i)*out_g*(1-out_g)*(out_o-y(i))*out_o*(1-out_o)*theta(42);
            temp_35=x5(i)*out_g*(1-out_g)*(out_o-y(i))*out_o*(1-out_o)*theta(42);
            theta(42)=theta(42)-(alpha*temp_42);
            theta(41)=theta(41)-(alpha*temp_41);
            theta(40)=theta(40)-(alpha*temp_40);
            theta(39)=theta(39)-(alpha*temp_39);
            theta(38)=theta(38)-(alpha*temp_38);
            theta(37)=theta(37)-(alpha*temp_37);
            theta(36)=theta(36)-(alpha*temp_36);
            theta(35)=theta(35)-(alpha*temp_35);
            theta(34)=theta(34)-(alpha*temp_34);
            theta(33)=theta(33)-(alpha*temp_33);
            theta(32)=theta(32)-(alpha*temp_32);
            theta(31)=theta(31)-(alpha*temp_31);
            theta(30)=theta(30)-(alpha*temp_30);
            theta(29)=theta(29)-(alpha*temp_29);
            theta(28)=theta(28)-(alpha*temp_28);
            theta(27)=theta(27)-(alpha*temp_27);
            theta(26)=theta(26)-(alpha*temp_26);
            theta(25)=theta(25)-(alpha*temp_25);
            theta(24)=theta(24)-(alpha*temp_24);
            theta(23)=theta(23)-(alpha*temp_23);
            theta(22)=theta(22)-(alpha*temp_22);
            theta(21)=theta(21)-(alpha*temp_21);
            theta(20)=theta(20)-(alpha*temp_20);
            theta(19)=theta(19)-(alpha*temp_19);
            theta(18)=theta(18)-(alpha*temp_18);
            theta(17)=theta(17)-(alpha*temp_17);
            theta(16)=theta(16)-(alpha*temp_16);
            theta(15)=theta(15)-(alpha*temp_15);
            theta(14)=theta(14)-(alpha*temp_14);
            theta(13)=theta(13)-(alpha*temp_13);
            theta(12)=theta(12)-(alpha*temp_12);
            theta(11)=theta(11)-(alpha*temp_11);
            theta(10)=theta(10)-(alpha*temp_10);
            theta(9)=theta(9)-(alpha*temp_9);
            theta(8)=theta(8)-(alpha*temp_8);
            theta(7)=theta(7)-(alpha*temp_7);
            theta(6)=theta(6)-(alpha*temp_6);
            theta(5)=theta(5)-(alpha*temp_5);
            theta(4)=theta(4)-(alpha*temp_4);
            theta(3)=theta(3)-(alpha*temp_3);
            theta(2)=theta(2)-(alpha*temp_2);
            theta(1)=theta(1)-(alpha*temp_1);
            %Backward Propagation End%
        end
        %forwardPropagation(x1(i),x2(i),y(i));
    end
    toc
end